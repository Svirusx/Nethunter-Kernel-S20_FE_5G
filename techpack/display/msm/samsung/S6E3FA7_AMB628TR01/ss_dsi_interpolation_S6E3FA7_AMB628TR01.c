	set_bit(BR_NORMAL_VINT, vdd->panel_br_info.br_info_select);
	set_bit(BR_NORMAL_ELVSS, vdd->panel_br_info.br_info_select);
	set_bit(BR_NORMAL_IRC, vdd->panel_br_info.br_info_select);

